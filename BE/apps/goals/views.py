class TaskListView(APIView):
    """
    API endpoint for task collection
    GET: List all tasks with optional filtering
    POST: Create a new task under a specific goal
    """

    permission_classes = [IsAuthenticated]

    def get(self, request):
        """
        Get list of all tasks with optional filtering
        Query params:
        - status: Filter by status (ToDo, InProgress, Completed)
        - goal_id: Filter by specific goal
        - search: Search in task name
        """
        # 1. Get filtered and sorted data
        tasks = TaskService.get_prepared_tasks(request.user, request.query_params)

        # 2. Serialize
        serializer = TaskSerializer(tasks, many=True)

        # 3. Response
        response_data = TaskService.build_task_list_response(serializer.data)

        return Response(response_data)

    def post(self, request):
        """
        Create a new task under a specific goal.
        Request body:
        {
            "goal_id": "<uuid>",   # required - must belong to the current user
            "name": "Task name",
            "deadline": "YYYY-MM-DD",
            "status": "ToDo"        # optional, defaults to ToDo
        }
        """
        goal_id = request.data.get("goal_id")
        if not goal_id:
            return Response(
                {"goal_id": "This field is required."},
                status=status.HTTP_400_BAD_REQUEST,
            )

        # Security: ensure the goal belongs to the current user
        goal = get_object_or_404(Goal, id=goal_id, user=request.user)

        serializer = TaskSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save(goal=goal)
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)
