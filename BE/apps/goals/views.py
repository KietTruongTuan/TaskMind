@extend_schema_view(
    get=extend_schema(
        tags=["Goals"],
        summary="List all goals",
        description="Get a list of all goals with optional filtering by status, search, tag, and date range.",
        responses={200: GoalListSerializer(many=True)},
    ),
    post=extend_schema(
        tags=["Goals"],
        summary="Create a new goal",
        description="Create a new goal with tasks.",
        request=GoalCreateSerializer,
        responses={201: GoalCreateSerializer},
    ),
)
class GoalView(APIView):
    """
    API endpoint for CRUD operations on goals
    GET: List all goals with optional filtering
    POST: Create a new goal
    """

    permission_classes = [IsAuthenticated]

    def get(self, request):
        """
        Get list of goals with optional filtering
        Query params:
        - status: Filter by status (ToDo, InProgress, Completed)
        - search: Search in name and description
        - tag: Filter by tag
        - startDate: Filter by start date
        - endDate: Filter by end date
        """
        try:
            # 1. Get filtered data
            goals = GoalService.get_filtered_goals(request.user, request.query_params)

            # 2. Serialize
            serializer = GoalListSerializer(goals, many=True)

            # 3. Build response payload (with counts)
            response_data = GoalService.build_goal_list_response(serializer.data)

            return Response(response_data)

        except Exception as e:
            print(f"CRASH IN GOALVIEW GET: {str(e)}")
            traceback.print_exc()
            return Response({"error": "Something went wrong!"}, status=500)

    def post(self, request):
        """
        Create a new goal
        Request body:
        {
            "name": "Goal name",
            "description": "Goal description",
            "deadline": "YYYY-MM-DD",
            "tag": "Goal tag"
        }
        """
        serializer = GoalCreateSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save(user=request.user)
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


@extend_schema_view(
    get=extend_schema(
        tags=["Goals"],
        summary="Get goal by ID",
        description="Retrieve a specific goal with all its tasks.",
        responses={200: GoalDetailSerializer},
    ),
    patch=extend_schema(
        tags=["Goals"],
        summary="Update a goal",
        description="Partially update a goal's details and/or tasks.",
        request=GoalDetailSerializer,
        responses={200: GoalDetailSerializer},
    ),
    delete=extend_schema(
        tags=["Goals"],
        summary="Delete a goal",
        description="Delete a goal and all its associated tasks.",
        responses={204: None},
    ),
)
class GoalDetailView(APIView):
    """
    API endpoint for CRUD operations on a single goal
    GET: Get a goal by ID
    PATCH: Partially update a goal by ID
    DELETE: Delete a goal by ID
    """

    permission_classes = [IsAuthenticated]

    def get_object(self, goal_id):
        return get_object_or_404(Goal, id=goal_id, user=self.request.user)

    def get(self, request, goal_id):
        """
        Get a goal by ID
        """
        goal = self.get_object(goal_id)
        serializer = GoalDetailSerializer(goal)
        return Response(serializer.data, status=status.HTTP_200_OK)

    def patch(self, request, goal_id):
        """
        Partially update a goal by ID
        Request body (all fields optional):
        {
            "name": "Goal name",
            "description": "Goal description",
            "deadline": "YYYY-MM-DD",
            "tag": ["tag1", "tag2"],
            "tasks": [{"id": 1, "name": "Updated task"}]
        }
        """
        goal = self.get_object(goal_id)
        serializer = GoalDetailSerializer(goal, data=request.data, partial=True)
        if serializer.is_valid():
            # update goal completed date if status = completed
            GoalService.prepare_goal_update(goal, serializer.validated_data)

            serializer.save()
            return Response(serializer.data, status=status.HTTP_200_OK)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

    def delete(self, request, goal_id):
        """
        Delete a goal and associated tasks by ID
        """
        goal = self.get_object(goal_id)
        goal.delete()
        return Response(status=status.HTTP_204_NO_CONTENT)


@extend_schema_view(
    get=extend_schema(
        tags=["Tasks"],
        summary="Get a task",
        description="Retrieve a specific task by ID.",
        responses={200: TaskSerializer},
    ),
    patch=extend_schema(
        tags=["Tasks"],
        summary="Update a task",
        description="Update a task's status, name, deadline.",
        request=TaskSerializer,
        responses={200: TaskSerializer},
    ),
    delete=extend_schema(
        tags=["Tasks"],
        summary="Delete a task",
        description="Delete a specific task from a goal.",
        responses={204: None},
    ),
)
class TaskDetailView(APIView):
    """
    API endpoint for CRUD operations on an individual task
    GET: Retrieve a task by ID
    PATCH: Update a task (quick status/name changes)
    DELETE: Delete a task
    """

    permission_classes = [IsAuthenticated]

    def get_object(self, pk):
        return get_object_or_404(Task, pk=pk, goal__user=self.request.user)

    def get(self, request, pk):
        """
        Retrieve a task by ID
        URL: GET /v1/tasks/{taskId}
        """
        task = self.get_object(pk)
        serializer = TaskSerializer(task)
        return Response(serializer.data, status=status.HTTP_200_OK)

    def patch(self, request, pk):
        """
        Update a task's status, name, or other attributes
        URL: PATCH /v1/tasks/{taskId}
        """
        task = self.get_object(pk)
        serializer = TaskSerializer(task, data=request.data, partial=True)

        if serializer.is_valid():
            TaskService.prepare_task_update(task, serializer.validated_data)
            updated_task = serializer.save()

            GoalService.sync_goal_completion_status(updated_task.goal)

            return Response(serializer.data, status=status.HTTP_200_OK)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

    def delete(self, request, pk):
        """
        Delete a task
        URL: DELETE /v1/tasks/{taskId}
        """
        task = self.get_object(pk)
        task.delete()
        return Response(status=status.HTTP_204_NO_CONTENT)


