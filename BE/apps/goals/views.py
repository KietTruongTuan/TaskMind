class TaskDetailView(APIView):
    """
    API endpoint for updating individual tasks
    PATCH: Update a task (quick status/name changes)
    DELETE: Delete a task
    """
    permission_classes = [IsAuthenticated]
    
    def get_object(self, pk, user):
        return get_object_or_404(Task, pk=pk, goal__user=user)
    
    def patch(self, request, goal_id, pk):
        """
        Update a task's status, name, or other attributes
        URL: PATCH /goals/<goal_id>/tasks/<task_id>
        """
        task = self.get_object(pk, request.user)
        serializer = TaskSerializer(task, data=request.data, partial=True)
        
        if serializer.is_valid():
            # Auto-set complete_date when status is changed to Completed
            if serializer.validated_data.get('status') == 'Completed' and task.status != 'Completed':
                serializer.validated_data['complete_date'] = timezone.now().date()
            
            serializer.save()
            return Response(serializer.data, status=status.HTTP_200_OK)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

    def delete(self, request, goal_id, pk):
        """
        Delete a task
        URL: DELETE /v1/goals/{goal_id}/tasks/{pk}
        """
        task = self.get_object(pk, request.user)
        task.delete()
        return Response(status=status.HTTP_204_NO_CONTENT)


@extend_schema_view(
    get=extend_schema(
        tags=['Tasks'],
        summary="List all tasks",
        description="Get all tasks for the authenticated user with optional filtering by status, goal_id, and search.",
        responses={200: TaskSerializer(many=True)}
    )
)
class TaskListView(APIView):
    """
    API endpoint to list all tasks for the authenticated user
    GET: List all tasks with optional filtering
    """
    permission_classes = [IsAuthenticated]

    def get(self, request):
        """
        Get list of all tasks with optional filtering
        Query params:
        - status: Filter by status (ToDo, InProgress, Completed)
        - goal_id: Filter by specific goal
        - search: Search in task name
        """
        # Get all tasks from user's goals
        tasks = Task.objects.filter(goal__user=request.user)

        # Apply filters
        status_filter = request.query_params.get('status', None)
        goal_id_filter = request.query_params.get('goal_id', None)
        search_query = request.query_params.get('search', None)

        if status_filter:
            tasks = tasks.filter(status=status_filter)

        if goal_id_filter:
            tasks = tasks.filter(goal_id=goal_id_filter)

        if search_query:
            tasks = tasks.filter(name__icontains=search_query)

        # Order by deadline (soonest first)
        tasks = tasks.select_related('goal').order_by('deadline', 'position')

        serializer = TaskSerializer(tasks, many=True)
        
        # Add goal_name to each task for context
        data = serializer.data
        for i, task in enumerate(tasks):
            data[i]['goal_name'] = task.goal.name
            data[i]['goal_id'] = task.goal.id

        return Response(data)